SET NOCOUNT ON

DECLARE @RESULT as TABLE (QUESTION varchar(255), ANSWER varchar(10))

IF (
    SELECT COUNT(*)
    FROM store.Clients
    ) > 0
 INSERT INTO @RESULT (QUESTION, ANSWER) SELECT 'Is Clients Populated?','YES' as RESULT
 ELSE 
 INSERT INTO @RESULT (QUESTION, ANSWER) SELECT 'Is Clients Populated?','NO' as RESULT


 -- PRINT RESULTS
SELECT *
FROM @RESULT

SET NOCOUNT OFF
